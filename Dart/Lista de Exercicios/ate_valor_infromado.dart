/**M01 - Lista - Exercício 16 - Até Valor Informado
Objetivo
Mostrar todos os números de 1 até o valor informado.

Instruções
Escreva um algoritmo em Dart que receba um valor.
Mostre todos os números de 1 até o valor informado.
Casos de Testes
Entrada: 5

Saída: 1, 2, 3, 4, 5. */

void main() {
  final num = 5;

  for (var i = 1; i <= num; i++) {
    print(i);
  }
}
